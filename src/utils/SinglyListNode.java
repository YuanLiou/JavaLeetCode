package utils;

public class SinglyListNode {
	private int val;
	private SinglyListNode next;

	public SinglyListNode(int value) {
		this.val = value;
	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

	public SinglyListNode getNext() {
		return next;
	}

	public void setNext(SinglyListNode next) {
		this.next = next;
	}
}
